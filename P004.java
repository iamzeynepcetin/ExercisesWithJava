//Verilen 2 integer sayinin birbirine bolumunu ve modunu bulan kod

class IntDivision {
	public static void main(String args[]) throws Exception
	{
		int x, y, z;
		x=17;
		System.out.println("x = "+x);
		y=5;
		System.out.println("y = "+y);

		z=x/y;
		System.out.println("x / y = "+z);
		z=x%y;
		System.out.println("x % y = "+z);
	}
}
