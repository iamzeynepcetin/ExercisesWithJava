class Matris17{
	public static void main(String args[])throws Exception{
		int m[][]=new int[25][25];
		int i,k;
		int row=25;
		int col=25;
		int a=(col)/2;
		for(i=0;i<row;i++){
			m[i][i]=1;
			m[i][col-i-1]=1;
			m[i][a]=1;
			m[a][i]=1;
			
		}
		for(i=0;i<row;i++){
			for(k=0;k<col;k++)
				System.out.print(m[i][k]+ " ");
			System.out.println();
		}
	}
}